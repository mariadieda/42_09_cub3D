

/*
A. Looking up and down.

Recall that the projection plane is 200 units high.
And up to this point, we always set the vertical center of the
projection plane to be exactly in the middle (that is, at point
y=100). Thus the midpoint of any wall slice will be drawn at
projection point y=100. It turns out that the effect of looking
up or down can be simulated simply by changing this value.

That is, to simulate looking up, instead of putting the center
of the vertical slice at y=100, we put it at a point where
y>100 (this is similar to moving the projection plane upward).

Similarly, to simulate looking down, instead of putting the
center of the vertical slice at y=100, we put it at a point
where y<100 (this is similar to moving the projection plane
downward).

 */